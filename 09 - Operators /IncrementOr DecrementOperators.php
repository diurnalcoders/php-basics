<?php 

// Pre-increment 
$x = 10;  
echo ++$x; // Increments $x by one, then returns $x 
echo "\n";

// Post-increment
$b = 10;
echo $b++;
echo "\n"; // Returns $x, then increments $x by one

// Pre-decrement
$y = 10; 
echo --$y; 
echo "\n"; // Decrements $x by one, then returns $x

// post-decrement
$j = 10; 
echo $j--;
echo "\n"; // // Decrements $x by one, then returns $x

?>
